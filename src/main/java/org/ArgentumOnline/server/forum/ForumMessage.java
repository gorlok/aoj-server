package org.ArgentumOnline.server.forum;

/**
 * @author gorlok
 */
public class ForumMessage {

	String title;
	String body;

	public ForumMessage(String title, String text) {
		this.title = title;
		this.body = text;
	}

	public String getTitle() {
		return this.title;
	}

	public String getBody() {
		return this.body;
	}

}
